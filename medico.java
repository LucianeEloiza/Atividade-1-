package CLINICAMEDICA;

public class medico {
	int codigo;
	String nome;
	String sexo;
	String especialidade;
	String endereço;
	
	public medico(int codigo, String nome, String sexo, String espeialidade, String endereço) {
		this.codigo = codigo;
		this.nome = nome;
		this.sexo = sexo;
		this.especialidade = espeialidade;
		this.endereço = endereço;
	}
	
	
	
	

}
